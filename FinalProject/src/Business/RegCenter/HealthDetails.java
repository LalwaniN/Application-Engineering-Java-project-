public class HealthDetails {
    
    private String bloodGroup;
    private int height;
    private float weight;
    private float bmi;
    private boolean hasHighBloodPressure;
    private boolean hasDiabetes;
    private boolean hasInfectiousDisease;
    private boolean familyHistOfDisease;
    private boolean psychiatricDisease;
    private boolean hasCancer;
    private boolean hasHIV;

    public String getBloodGroup() {
        return bloodGroup;
    }

    public void setBloodGroup(String bloodGroup) {
        this.bloodGroup = bloodGroup;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public float getBmi() {
        return bmi;
    }

    public void setBmi(float bmi) {
        this.bmi = bmi;
    }

    public boolean isHasHighBloodPressure() {
        return hasHighBloodPressure;
    }

    public void setHasHighBloodPressure(boolean hasHighBloodPressure) {
        this.hasHighBloodPressure = hasHighBloodPressure;
    }

    public boolean isHasDiabetes() {
        return hasDiabetes;
    }

    public void setHasDiabetes(boolean hasDiabetes) {
        this.hasDiabetes = hasDiabetes;
    }

    public boolean isHasInfectiousDisease() {
        return hasInfectiousDisease;
    }

    public void setHasInfectiousDisease(boolean hasInfectiousDisease) {
        this.hasInfectiousDisease = hasInfectiousDisease;
    }

    public boolean isFamilyHistOfDisease() {
        return familyHistOfDisease;
    }

    public void setFamilyHistOfDisease(boolean familyHistOfDisease) {
        this.familyHistOfDisease = familyHistOfDisease;
    }

    public boolean isPsychiatricDisease() {
        return psychiatricDisease;
    }

    public void setPsychiatricDisease(boolean psychiatricDisease) {
        this.psychiatricDisease = psychiatricDisease;
    }

    public boolean isHasCancer() {
        return hasCancer;
    }

    public void setHasCancer(boolean hasCancer) {
        this.hasCancer = hasCancer;
    }

    public boolean isHasHIV() {
        return hasHIV;
    }

    public void setHasHIV(boolean hasHIV) {
        this.hasHIV = hasHIV;
    }
    
    
    
    
}
